#include <stdio.h>

int main() {
	printf("%4s \n", "*"); //Prints a star after 4 spaces
	printf("%5s \n", "***"); //Prints 3 stars after 5 spaces
	printf("%6s \n", "*****"); //Prints 5 stars after 6 spaces
	printf("******* \n"); //Prints 7 stars
	printf("%5s \n", "***"); //Prints 3 stars after 5 spaces

	return 0;
}